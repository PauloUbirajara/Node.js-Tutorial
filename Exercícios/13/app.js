// Versionamento de pacotes npm //
// Exemplo: lodash: ^4.17.11 -> major.minor.patch

// ^ -> não permite que realize updates do tipo 'major'
// ~ -> permite apenas updates do tipo 'patch'
// Sem símbolo na frente -> não permite atualizações novas do pacote

// major -> atualização que torna algumas funções antigas inoperantes
// minor -> novas funções/alterações onde não acontece erros devido a funções antigas.
// patch -> bug fixes, pequenas alterações